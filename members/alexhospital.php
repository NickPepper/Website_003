<H1 align="center">АЛЕКСАНДРОВСКАЯ БОЛЬНИЦА</H1>
<p align="center"><a href="http://www.alexhospital.ru/" target="_blank">www.alexhospital.ru</a></p>
<p align="justify">16 апреля 1842 г. государем-императором Николаем I было утверждено мнение Государственного совета &laquo;Об учреждении в Санкт-Петербурге постоянной больницы для чернорабочих&raquo; на 470 коек. Однако здание для больницы не было построено, и она размещалась в нескольких помещениях: в здании Исправительного дома, расположенного у места слияния рек Пряжки и Мойки, в здании лейб-гвардии Измайловского полка, в купленных домах в разных районах Санкт-Петербурга. Лечебное заведение, таким образом, оказалось разбросанным чуть ли не по всему городу. 22 марта 1861 г. Санкт-Петербургская городская дума в связи с реформой (освобождение крестьян от крепостной зависимости) постановила учредить больницу для чернорабочих, наименовав ее &laquo;Александровская больница для рабочего населения в память 19 февраля 1861 г.&raquo;.</p>
<p align="justify">В настоящее время Городская Александровская больница является современным многопрофильным стационаром, где ежегодно получают лечение около 40 тысяч петербуржцев и гостей города. Пациенты получают все виды необходимых обследований и лечебных процедур.</p>
<p align="justify">Больница активно сотрудничает с кафедрами ведущих медицинских вузов города, такими как кафедра урологии и андрологии МАПО, кафедра неотложной медицины МАПО, общей и военно-морской хирургии ВМА, акушерства и гинекологии ВМА, акушерства и гинекологии МАПО, акушерства и гинекологии Академии им. Мечникова.</p>
